/* Includes ------------------------------------------------------------------------*/
#include "includes.h"


/* Task ID -------------------------------------------------------------------*/
OS_TID tid_Time_Counter;	// ID задачи t_Time_Count
	
	
/* Global variables ----------------------------------------------------------------*/
U32 unixtime = 0;
U32 localtime = 0;


/*----------------------------------------------------------------------------
 *        Task 't_Time_Count' * Time count *
 *----------------------------------------------------------------------------*/
void t_Time_Counter(void)
{
	for(;;)
	{
		os_dly_wait(1000);
		unixtime++;
		localtime++;
		
		// Reload IWDG counter 
		IWDG_ReloadCounter();
	}
}

	
	
	




